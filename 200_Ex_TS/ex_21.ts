let a: number = parseInt(prompt("Digite o número do mês."));
if (a <= 3) {
	console.log("A estação atual é Verão.");
}
else if (a <= 6) {
	console.log("A estação atual é Outono.");
}
else if (a <= 9) {
	console.log("A estação atual é Inverno.")
}
else if (a <= 12) {
	console.log("A estação atual é Primavera.")
}
